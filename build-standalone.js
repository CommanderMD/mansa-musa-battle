/*
 * build-standalone.js — bundles the ES-module game into ONE classic-script HTML file
 * that runs from file:// with zero local fetches (Phaser still loads from CDN over https).
 *
 * It strips `import ...` lines and the `export ` keyword from each module, concatenates
 * them in dependency order inside a single IIFE, and injects that into the index.html
 * shell in place of the `<script type="module">` tag.
 *
 *   node build-standalone.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SRC = path.join(ROOT, 'public', 'src');

// Dependency order: constants/data first, then systems, entities, scenes, then bootstrap.
const ORDER = [
  'config.js',
  'data/chapters.js',
  'systems/juice.js',
  'systems/audio.js',
  'systems/textures.js',
  'systems/Lane.js',
  'entities/Crowd.js',
  'entities/Gate.js',
  'entities/EnemyFormation.js',
  'entities/Pickup.js',
  'scenes/BootScene.js',
  'scenes/MapScene.js',
  'scenes/BattleScene.js',
  'scenes/ResultScene.js',
  'main.js',
];

function strip(code) {
  return code
    .replace(/^\s*import\s.*$/gm, '') // drop ES import statements
    .replace(/^export\s+/gm, ''); // drop the `export ` keyword, keep the declaration
}

let bundle = '';
for (const rel of ORDER) {
  const file = path.join(SRC, rel);
  const code = strip(fs.readFileSync(file, 'utf8'));
  bundle += `\n/* ===== ${rel} ===== */\n` + code + '\n';
}

// Sanity checks — fail loudly if anything that breaks file:// slipped through.
if (/^\s*import\s/m.test(bundle)) throw new Error('residual import statement in bundle');
if (/^\s*export\s/m.test(bundle)) throw new Error('residual export statement in bundle');
if (/\.load\.(image|audio|json|spritesheet|atlas|video|binary|bitmapFont|svg)\b/.test(bundle))
  throw new Error('found a local asset loader call — inline it before bundling');

const wrapped =
  '<script>\n(function () {\n"use strict";\n' +
  bundle +
  '\n})();\n</script>';

// Inject into the index.html shell, replacing the module script tag.
let html = fs.readFileSync(path.join(ROOT, 'public', 'index.html'), 'utf8');
html = html.replace(
  /<script type="module" src="\.\/src\/main\.js"><\/script>/,
  wrapped
);
// Add a small note in the title comment.
html = html.replace(
  '<title>',
  '<!-- Single-file standalone build — generated by build-standalone.js. Open by double-clicking (file://). -->\n  <title>'
);

if (html.includes('type="module"')) throw new Error('a module script tag survived');

const OUTS = process.argv.slice(2);
if (!OUTS.length) OUTS.push(path.join(ROOT, 'mansa-musa-battle-standalone.html'));
for (const out of OUTS) {
  fs.mkdirSync(path.dirname(out), { recursive: true });
  fs.writeFileSync(out, html);
  console.log('wrote', out, '(' + (html.length / 1024).toFixed(1) + ' KiB)');
}
